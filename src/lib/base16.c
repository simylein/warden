int base16_encode(char *buf, const size_t buf_len, const void *bin, const size_t bin_len) {
	if (buf_len != bin_len * 2) {
		return -1;
	}

	size_t buf_ind = 0;
	size_t bin_ind = 0;
	while (bin_ind < bin_len) {
		const uint8_t byte = ((uint8_t *)bin)[bin_ind++];
		buf[buf_ind++] = charset[byte >> 4];
		buf[buf_ind++] = charset[byte & 0x0f];
	}

	return 0;
}

int base16_decode(void *bin, const size_t bin_len, const char *buf, const size_t buf_len) {
	if (bin_len < buf_len / 2) {
		return -1;
	}

	size_t bin_ind = 0;
	size_t buf_ind = 0;
	while (buf_ind + 1 < buf_len) {
		uint8_t high = lookup[(uint8_t)buf[buf_ind++]];
		uint8_t low = lookup[(uint8_t)buf[buf_ind++]];

		if (high == 0xff || low == 0xff) {
			return -1;
		}

		((uint8_t *)bin)[bin_ind++] = (uint8_t)(high << 4 | low);
	}

	return 0;
}
